package Collection;

public class Boxing {
	public static void main(String[] args) {
		Integer i = new Integer(10);
		int a = i;
		System.out.println(a);
		
		Character ch = new Character('a');
		char c = ch;
		System.out.println(c);
		
		Byte B  = 10;
		byte b = B;
		System.out.println(b);
		
	}

}
